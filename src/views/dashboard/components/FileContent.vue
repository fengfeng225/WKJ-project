      <p>12.班组盲板、三阀组及放空阀封堵台账中隔离的介质应与实际相符，介质压力和温度与实际接近。偏差过大或不符每处考核-0.5分/处。</p>
      <p>13.每块盲板都需要建立对应的盲板流程图，车间发现盲板流程图与现场及台账不符的，工艺考核承包班组-0.3分/处</p>
