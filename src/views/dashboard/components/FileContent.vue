      <p>1. 工艺副主任是车间盲板、放空阀封堵和防互窜管理负责人，负责组织制订安全可靠的管理方案并监督执行；装置工艺负责人是装置的盲板、放空阀封堵和防互窜管理的业务主管，负责下达作业指令，对方案执行情况进行现场监督、检查、登记和考核，做到管理受控；装置工艺、设备技术员为盲板、放空阀封堵和防互窜管理的业务主办，负责配合业务主管开展管理工作，主管不在岗期间履行主管职责。</p>
