      <p>3.车间发现台账与现场牌子或投用状态不符的，牌子丢失（特殊原因如大风除外）或牌号不清楚的，盲板为非标准盲板的，盲板规格和型号不符合要求的。工艺考核承包班组-0.5分/处。</p>
      <p>4.班组进行作业时导通的盲板或三阀组待作业结束后需及时恢复，未及时恢复的工艺考核班组-0.5分/处，并取消任务分。若作业由多个班组依次完成，则恢复工作由最后一个班组进行。</p>
      <p>5.当班拆装的盲板需挂盲板牌（拆出盲板需摘牌并将牌子统一收回）并及时录入班长记录、盲板拆装台账，并将盲板台账更新。未执行的工艺考核班组-0.5分/处。</p>
      <p>6.对于不再使用的盲板或三阀组，承包班组要及时在台账上标注清晰，回收保存好牌子，如果盲板或三阀组牌子丢失，工艺考核-0.5分/处，且班组自行按标准配齐。</p>
