      <p>2. 其中Ⅰ、Ⅱ、Ⅲ套高压加氢和Ⅱ套白油加氢的压缩机入口氮气三阀组全部按盲板管理，除机组需大修或车间安排的特殊情况下盲板可以为“通”位，其余情况下盲板均在“盲”位。Ⅰ、Ⅱ、Ⅲ套高压加氢事故氮气三阀组，按照每周检查一次的频率，班组通用-006防护窜台账-未封堵放空阀表，如后期车间安排事故氮气三阀组盲板由“通”全部变成“盲”位后，可不用继续填写。</p>

