      <h2>三、检查内容</h2>
      <p>1.盲板牌完好、牌号清晰可见，现场牌号、盲板（“盲”/“通”）状态与台账一致。</p>
      <p>2.盲板为标准盲板，且应优先选用标准的“8”字盲板。</p>
      <p>3.盲板（堵头/管堵）规格、型号等应与盲板台账一致，且符合对当前管线里的介质的隔离要求。</p>
      <p>4.盲板安装正确，盲板俩侧均应有合适且标准的垫片，盲板俩侧无泄漏，俩侧法兰螺栓均满扣。</p>
      <p>5. 堵头、管堵安装正确，紧固且无明显泄漏。</p>
      <p>6.隔离介质为易燃易爆有毒的介质，特别是气体时，应携带相应的检测仪，检测盲板、堵头、管堵有无泄露情况。</p>
      <p>7.班组盲板、三阀组及放空阀封堵台账中隔离的介质，介质压力和温度与实际接近。</p>
      <p>8.各班组按照《中石油克拉玛依石化有限责任公司盲板、放空阀封堵管理办法》仔细检查本班承包区物料封堵、能量隔离的完善情况，发现问题应及时反馈并整改。</p>
      <p>9.每块盲板都需要建立对应的盲板流程图，车间盲板流程图与现场及台账都要符合。</p>

